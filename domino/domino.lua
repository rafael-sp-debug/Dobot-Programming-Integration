-- Version: Lua 5.3.5
DhInit(115200,1)
Speed(100)
DhOpen(0,1)
Sync()

function domino(origin, pickup, lift, goal, dropoff, finish)
  Go(P65)
  Go(origin)
  DhOpen(0,1)
  Sleep( math.ceil(1 * 1000) )
  Go(pickup)
  DhClose(0,1)
  Sleep( math.ceil(1 * 1000) )
  Go(lift)
  Go(goal)
  Sleep( math.ceil(1 * 500) )
  Go(dropoff)
  DhOpen(0,1)
  Sleep( math.ceil(1 * 1000) )
  Go(finish)
  Sleep( math.ceil(1 * 1000) )
end

